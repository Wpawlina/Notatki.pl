        <h1>Regulamin użytkowania serwisu "Moje Notatki"</h1>
        <p>Prosimy o przeczytanie poniższych zasad korzystania z serwisu "Moje Notatki" przed rozpoczęciem korzystania z naszych usług.</p>

        <h2>1. Akceptacja regulaminu</h2>
        <p>Korzystając z serwisu "Moje Notatki", użytkownik zgadza się na postanowienia niniejszego regulaminu oraz zobowiązuje się do jego przestrzegania.</p>

        <h2>2. Cel serwisu</h2>
        <p>"Moje Notatki" jest platformą internetową umożliwiającą użytkownikom przechowywanie, zarządzanie i udostępnianie swoich notatek oraz innych treści.</p>

        <h2>3. Rejestracja</h2>
        <ol>
            <li>Aby korzystać z pełnych funkcjonalności serwisu, użytkownik musi się zarejestrować, podając prawdziwe dane osobowe.</li>
            <li>Użytkownik jest odpowiedzialny za zachowanie poufności swojego hasła oraz za wszelkie czynności dokonane za jego pośrednictwem.</li>
        </ol>

        <h2>4. Treści użytkowników</h2>
        <ol>
            <li>Użytkownik jest wyłącznym właścicielem swoich notatek i innych treści dodanych do serwisu.</li>
            <li>Użytkownik zobowiązuje się do nieumieszczania treści naruszających prawa autorskie, prawa własności intelektualnej lub inne przepisy prawne.</li>
            <li>Administracja serwisu zastrzega sobie prawo do usuwania treści naruszających regulamin oraz do zablokowania dostępu użytkownikowi, który łamie jego postanowienia.</li>
        </ol>

        <h2>5. Ochrona danych osobowych</h2>
        <ol>
            <li>Serwis "Moje Notatki" przestrzega obowiązujących przepisów dotyczących ochrony danych osobowych.</li>
            <li>Dane osobowe użytkowników są przechowywane i przetwarzane zgodnie z polityką prywatności dostępną na stronie serwisu.</li>
        </ol>

        <h2>6. Odpowiedzialność</h2>
        <ol>
            <li>Administracja serwisu nie ponosi odpowiedzialności za treści dodane przez użytkowników.</li>
            <li>Użytkownik ponosi pełną odpowiedzialność za treści, które umieszcza w serwisie oraz za wszelkie szkody wynikłe z ich umieszczenia.</li>
            <li>Serwis "Moje Notatki" nie ponosi odpowiedzialności za przerwy w działaniu serwisu spowodowane awariami technicznymi, działaniami osób trzecich ani innymi niezależnymi od niego czynnikami.</li>
        </ol>

        <h2>7. Zmiany w regulaminie</h2>
        <ol>
            <li>Administracja serwisu zastrzega sobie prawo do zmiany niniejszego regulaminu w dowolnym momencie.</li>
            <li>Zmiany w regulaminie będą publikowane na stronie serwisu i wchodzą w życie natychmiast po ich opublikowaniu.</li>
        </ol>

        <h2>8. Kontakt</h2>
        <p>W razie pytań lub wątpliwości dotyczących regulaminu, prosimy o kontakt za pośrednictwem formularza kontaktowego dostępnego na stronie serwisu.</p>

        <p>Dziękujemy za korzystanie z serwisu "Moje Notatki"!</p>
            
            
            
